<template>
  <div class="list">
    <div class="tableHead">
      <el-button
        class="coverBut"
        type="success"
        size="medium"
        @click="routingHop('/jinSwim/operation/carriage/compile')"
      >添加</el-button>
    </div>
    <el-table
      v-loading="listLoading"
      :data="list"
      element-loading-text="Loading"
      border
      fit
      highlight-current-row
    >
      <el-table-column align="center" label="序号" width="95">
        <template slot-scope="scope">
          {{ scope.$index + 1 }}
        </template>
      </el-table-column>
      <el-table-column label="模板名称">
        <template slot-scope="scope">
          <span style="cursor: pointer" @click="freightDetails(scope.row.freightId)">{{ scope.row.freightName }}</span>
        </template>
      </el-table-column>
      <el-table-column label="计费方式" width="150" align="center">
        <template slot-scope="scope">
          <span>{{ scope.row.freightChargingType || '暂无'}}</span>
        </template>
      </el-table-column>
      <el-table-column label="更新时间" width="250" align="center">
        <template slot-scope="scope">
          {{ scope.row.updatedOn || '暂无' }}
        </template>
      </el-table-column>
      <el-table-column
        align="center"
        label="操作"
        width="150"
      >
        <template slot-scope="scope">
          <el-button type="text" size="small" @click="editFreight(scope.row.freightId)">编辑</el-button>
          <el-button type="text" size="small" @click="delFreight(scope.row.freightId)">删除</el-button>
        </template>
      </el-table-column>
    </el-table>
    <el-pagination
      class="page"
      background
      @size-change="handleSizeChange"
      @current-change="handleCurrentChange"
      :current-page="pages.pageIndex"
      layout="sizes, total, prev, pager, next, jumper"
      :total="pages.total"
    >
    </el-pagination>
  </div>
</template>

<script>
  import { getFreightList, delFreight} from '@/api/Operation/carriage'

  export default {
    filters: {
      statusFilter(status) {
        switch (status) {
          case 0:
            return '未提交'
            break;
          case 1:
            return '待审核'
            break;
          case 2:
            return '通过'
            break;
          case 3:
            return '不通过'
            break;
        }
      },
      statusType(status) {
        switch (status) {
          case 0:
            return ''
            break;
          case 1:
            return 'warning'
            break;
          case 2:
            return 'success'
            break;
          case 3:
            return 'danger'
            break;
        }
      }
    },
    data() {
      return {
        list: null, // 渲染列表
        listLoading: true, // 加载
        pages:{
          pageSize: 10, // 每页多少条
          pageCount: 1, // 一共多少页
          pageIndex: 1, // 当前页数
          total: 0, // 总页数
        },
      }
    },
    created() {
      this.fetchData()
    },
    methods: {
      clear(){
        this.freightName = ''
        this.affiliatingArea = ''
        this.status = ''
      },
      handleChange(value) {
        console.log(value);
      },
      fetchData() {
        this.listLoading = true
        let params = {
          curPage: this.pages.pageIndex,
          pageSize: this.pages.pageSize,
          isDeleted: 0,
          isDisabled: 0
        }
        getFreightList(params)
          .then( res => {
            let data = res.data
            if (data.code == '1'){
              if (data.data.length){
                this.list = data.data
                this.pages.total = data.page.total
              }
            } else {
              this.$message.error(data.msg)
            }
            this.listLoading = false
          })
          .catch( err => {
            this.listLoading = false
          })
      },
      // 删除
      delFreight(id){
        this.$confirm('此操作将删除该数据, 是否继续?', '提示', {
          confirmButtonText: '确定',
          cancelButtonText: '取消',
          type: 'warning'
        })
        .then( () => {
          let params = {
            freightId: id
          }
          delFreight(params)
            .then( res => {
              if (res.data.code == '1') {
                this.$message.success(res.data.msg)
              }else {
                this.$message.error(res.data.msg)
              }
            })
            .catch( err => {
              console.log(err)
            })
        })
      },
      // 切换页数
      handleCurrentChange(val) {
        this.pages.pageIndex = val
        this.fetchData()
      },
      // 切换每页条数
      handleSizeChange(val) {
        this.pages.pageSize = val
        this.fetchData()
      },
      // 跳转
      routingHop(path){
        this.$router.push({
          path
        })
      },
      editFreight(freightId){
        this.$router.push({
          path:'/jinSwim/operation/carriage/compile',
          query:{
            freightId
          }
        })
      },
      freightDetails(freightId){
        this.$router.push({
          path:'/jinSwim/operation/carriage/details',
          query:{
            freightId
          }
        })
      }
    }
  }
</script>

